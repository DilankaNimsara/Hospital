    void combo() {
        try {
            String sql = "select date from drughistory where patientHname='" + Hopsitalno + "'ORDER BY date DESC";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            while (rs.next()) {
                String item1 = rs.getString("date");
                jComboBox1.addItem(item1);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    void search() {

        try {

            Statement st = con.createStatement();
            ResultSet rs6 = st.executeQuery("select * from barcode where img_title = '" + Hopsitalno + "'");
            if (rs6.next()) {
                byte[] img = rs6.getBytes("img_data");

                //Resize The ImageIcon
                ImageIcon image = new ImageIcon(img);
                Image im = image.getImage();
                Image myImg = im.getScaledInstance(jLabel6.getWidth(), jLabel6.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon newImage = new ImageIcon(myImg);
                jLabel6.setIcon(newImage);
            }

            String sql1 = "select * from patient where HospitalNumber='" + Hopsitalno + "'";
            ps = con.prepareStatement(sql1);
            rs = ps.executeQuery();
            if (rs.next()) {
                String add1 = rs.getString("HospitalNumber");
                jTextField1.setText(add1);
                String add9 = rs.getString("ClinicNumber");
                jTextField2.setText(add9);
                String add2 = rs.getString("Name");
                jTextField3.setText(add2);
            }

            String sql = "select * from drug where patientHname='" + Hopsitalno + "' ";
            PreparedStatement ps2 = con.prepareStatement(sql);
            ResultSet rs2 = ps2.executeQuery();
            if (rs2.next()) {
                String add2 = rs2.getString("drugslist");
                jTextArea2.setText(add2);
                druglist = (add2.replace("\n", " ").replace("\r", ""));
                String add1 = rs2.getString("date");
                jTextField4.setText(add1);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }

